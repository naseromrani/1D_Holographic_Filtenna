%=================================================================================================================================================
%                                                   Wave impedance for TE10-mode in a waveguide
%=================================================================================================================================================

clear 
close all
clc

%%% get inputs====================================================================================================================================

f=1e9*input('Enter the desired frequency "f" in GHz: ');                % ... desired frequency
Wsiw=1e-3*input('Enter the substrate thickness "Wsiw" in mm: ');        % ... SIW width
er=input('Enter the substrate relative permittivity "eps_r": ');        % ... dielectric relative permittivity

%%% define variables================================================================================================================================

d=0.733;
s=1.333;
e0=(1/(36*pi))*1e-7;
mu0=4*pi*1e-7;

%%% calculations====================================================================================================================================

Weff=Wsiw-d^2/(0.95*s);
k0=2*pi*f/3e8;
eta=sqrt(mu0/(e0*er));
Z=k0*eta/sqrt(k0^2-(pi/Weff)^2);

%%% display====================================================================================================================================

display(Z,'Z')


























